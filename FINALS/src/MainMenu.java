    private void btnCheckActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCheckActionPerformed
        
        try {
            
            String user_Balance = txtAccountID.getText();
            
            if (user_Balance.length() > 0) {
                
                String sql = "SELECT * FROM accounts WHERE user_id = '" + user_Balance + "' ";
                
                Connection con = DriverManager.getConnection("jdbc:mysql://127.0.0.1:3306/signup_info", "root", "");
                
                PreparedStatement s = con.prepareStatement(sql);
                ResultSet rs = s.executeQuery(sql);
                
                if (rs.next()) {
                    
                    String userBalance = rs.getString(9);
                    
                    phMoney.setText(userBalance);
                        
                } else {
                    
                    JOptionPane.showMessageDialog(rootPane, "You Account ID is Incorrect.");
                    
                }
                
            } 
            
        } catch (Exception ex) {
            
        }
        
    }//GEN-LAST:event_btnCheckActionPerformed

